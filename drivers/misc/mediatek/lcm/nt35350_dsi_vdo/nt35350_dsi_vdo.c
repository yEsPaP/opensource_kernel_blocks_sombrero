static void lcm_get_params(LCM_PARAMS *params)
{
	memset(params, 0, sizeof(LCM_PARAMS));

	params->type = LCM_TYPE_DSI;
	params->width = FRAME_WIDTH;
	params->height = FRAME_HEIGHT;

	params->physical_width = PHYSICAL_WIDTH * FRAME_WIDTH / LCM_FRAME_WIDTH;
	params->physical_height = PHYSICAL_HEIGHT * FRAME_HEIGHT / LCM_FRAME_HEIGHT;

	params->dsi.mode = SYNC_EVENT_VDO_MODE;//CMD_MODE;
	params->dsi.LANE_NUM = LCM_ONE_LANE;//LCM_TWO_LANE;
	params->dsi.data_format.format = LCM_DSI_FORMAT_RGB888;
	params->dsi.PS = LCM_PACKED_PS_24BIT_RGB888;
	
	// enable tearing-free
    params->dbi.te_mode 				= LCM_DBI_TE_MODE_DISABLED;//LCM_DBI_TE_MODE_VSYNC_ONLY;//
    params->dbi.te_edge_polarity		= LCM_POLARITY_RISING;
	


	params->dsi.vertical_sync_active = 4;//3;
	params->dsi.vertical_backporch = 2;//44;
	params->dsi.vertical_frontporch = 10;//44 + (LCM_FRAME_HEIGHT - FRAME_HEIGHT);
	params->dsi.vertical_active_line = FRAME_HEIGHT;

	params->dsi.horizontal_sync_active = 20;//10;
	params->dsi.horizontal_backporch = 20;//50;
	params->dsi.horizontal_frontporch = 20;//50 + (LCM_FRAME_WIDTH - FRAME_WIDTH);
	//params->dsi.horizontal_blanking_pixel = 60;
	params->dsi.horizontal_active_pixel = FRAME_WIDTH;

	params->dsi.PLL_CLOCK = 110;//fps 60
}

